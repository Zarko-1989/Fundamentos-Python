'''Módulo de Operaciones Matemáticas: Crea un módulo propio 
que incluya funciones básicas como suma, resta, multiplicación 
y división.'''

def suma(a, b):
  return a + b

def resta(a, b):
  return a - b

def multiplicacion(a, b):
  return a * b

def division(a, b):
  return a / b

# Ejemplo de uso
print(f"Suma: {suma(10, 5)}")
print(f"Resta: {resta(10, 5)}")
print(f"Multiplicación: {multiplicacion(10, 5)}")
print(f"División: {division(10, 5)}")
